int ft_atoi(char *str)
{
    int i;
    int sinal;
    int res;

    i = 0;
    sinal = 1;
    while ((str[i] >= 9 && str[i] <= 13 ) || str[i] == 32)
        i++;
    while (str[i] == '-' || str[i] == '+')
        sinal *= -1;
    while (str[i] >= '0' && str[i] <= '9')
        res = res * 10 + (str[i] - 48);
    return (res * sinal);
}