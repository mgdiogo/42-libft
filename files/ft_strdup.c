#include "libft.h"

char	*ft_strdup(const char *s)
{
	char	*d;
	int		i;

	d = (char *)malloc(ft_strlen(s) + 1);
	i = 0;
	if (!d)
		return (NULL);
	while (s[i])
	{
		d[i] = s[i];
		i++;
	}
	d[i] = '\0';
	return (d);
}
